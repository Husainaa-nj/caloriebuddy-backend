def create_text_chunks(data):
    chunks = []
    for entry in data:
        chunk = (
            f"{entry['quantity']} of {entry['item']} contains approximately {entry['calories']} calories, "
            f"with {entry['protein_g']}g protein, {entry['carbs_g']}g carbohydrates, and {entry['fat_g']}g fat. "
            f"Notable nutrients include: {entry['nutrient_highlights']}. This is usually consumed as {entry['meal_type']}."
        )
        chunks.append(chunk)
    return chunks

